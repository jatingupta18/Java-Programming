package C1_Basic_of_java;

public class P3_VariablesBasics {
    public static void main(String[] args) {
        int x;       // decleration
        x = 5;       // initialization
        int y = 20;
        System.out.println(x);
        System.out.println(y);
        System.out.println(x+y);
        System.out.println(x*y);

        x=70;
        System.out.println(x);

        // note: ham same name ke variable ko 1 times hi decler karte sakte hai.
        // but initialization kitne bhi baar kar sakte hai.
    }
}
