package C1_Basic_of_java;

public class P4_ModifyingValuesOfVariables {
    public static void main(String[] args) {
        int x = 7;
        System.out.println(x);
        x = x + 20;
        System.out.println(x);
        x = x - 7;
        System.out.println(x);
        x = x * 5;
        System.out.println(x);
        x = x / 5;
        System.out.println(x);
    }
}
