package C1_Basic_of_java;

public class P5_ArithmeticOperationOnInt {
    public static void main(String[] args) {
        double x = 17, y = 3;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        // 17/3 will be 5.6 but output will be 5 how ?
        // Ans: it will give integer value as we use int data type

        // To print decimal value we have to use double data type as double x = 17;
        // we can also use float data type as float x = 17f; but isko ham use nahi karenge

    }
}

