package C2_If_Else;

public class P1_RelationalOperators {
    public static void main(String[] args) {

        // Relational operator
        System.out.println(5>3);
        System.out.println(5<3);
        System.out.println(5+2 >= 7);
        System.out.println(5+2 != 7);

        // Boolean data type

        boolean x = true;
        System.out.println(x);

        boolean y = false;
        System.out.println(y);

        boolean z = (17 > 18);
        System.out.println(z);


    }
}





